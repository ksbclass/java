// 한줄 주석
/*
 * 1. public class HelloWorld => 파일의 이름과 public class의 이름이 같아야 한다
 * 2. 한개의 .java 파일 내에는 한개의 public class만 존재해야 한다.
 * 3. 대소문자를 구분함 : public,Public 다른 단어임. 
 * 4. 자바의 소스파일의 확장자는 .java 임
 */
public class HelloWorld {
	public static void main(String[] args) {
		System.out.println("Hello World!");
	}
}
